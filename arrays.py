#Array list
arr = [5, 10, 15, 20, 25]
print("Array list:", arr)

#Inserting 12 into the list at index 2
arr.insert(2, 12) 
print("After inserting 12 at index 2:", arr)

#Deleting last item
last_item = arr.pop()
print("After removing the last item:", last_item, arr)

#Final list
print("Final list:", arr)